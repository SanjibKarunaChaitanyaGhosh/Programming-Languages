/*
1. Arithmatic
    +,-,*,/,%
*/
 
class Arithmatic
{
    public static void main(String args[])
    {
        int a=20, b=6;
        System.out.println("a= "+a);
        System.out.println("b= "+b);

        System.out.println("a+b= "+(a+b));
        System.out.println("a-b= "+(a-b));
        System.out.println("a*b= "+(a*b));
        System.out.println("a/b This is Quotient= "+(a/b));
        System.out.println("a%b, this is nothing but Remainder = "+(a%b));
    }
}


